const asciiRender = (matrix: Matrix) => {
  const ASCII = new Map<0 | 1 | null, string>([
    [0, '░'],
    [1, '▓'],
    [null, '·'],
  ])

  return (
    '\n' +
    matrix.map(row => row.map(pixel => ASCII.get(pixel)).join(' ')).join('\n')
  )
}

