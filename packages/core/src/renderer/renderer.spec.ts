const potato = (x: number, y: number) => {
  throw new Error(`Got undefined under (${x},${y})`)
}

const asciiRender = (matrix: Matrix) => {
  const ASCII = new Map<0 | 1 | null, string>([
    [0, '░'],
    [1, '▓'],
    [null, '·'],
  ])

  return (
    '\n' +
    matrix
      .map((row, y) =>
        row
          .map((pixel, x) =>
            pixel === undefined ? potato(x, y) : ASCII.get(pixel)
          )
          .join(' ')
      )
      .join('\n')
  )
}

